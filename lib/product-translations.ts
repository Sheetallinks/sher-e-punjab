// Helper function to generate translation key from product name
function generateTranslationKey(productName: string): string {
  return productName
    .toLowerCase()
    .replace(/[()]/g, "")
    .replace(/\s+/g, "_")
    .replace(/\//g, "/") // Keep forward slash
    .replace(/[^a-z0-9_/]/g, "")
}

// Get translated product name
export function getTranslatedProductName(productName: string, language: Language): string {
  const key = generateTranslationKey(productName)
  const translation = productTranslations[key]?.[language]
  return translation || productName // Fallback to original name if translation not found
}

